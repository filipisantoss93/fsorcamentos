begin;

create table if not exists public.veiculo_marcas_catalogo (
  id bigserial primary key,
  nome text not null,
  ativo boolean not null default true,
  created_at timestamptz not null default now(),
  constraint veiculo_marcas_catalogo_nome_unique unique (nome)
);

create table if not exists public.veiculo_modelos_catalogo (
  id bigserial primary key,
  marca_id bigint not null references public.veiculo_marcas_catalogo(id) on delete cascade,
  nome text not null,
  tipo text not null default 'carro',
  ativo boolean not null default true,
  created_at timestamptz not null default now(),
  constraint veiculo_modelos_catalogo_tipo_check check (
    tipo in ('carro', 'picape', 'van_utilitario', 'caminhao_utilitario', 'moto')
  ),
  constraint veiculo_modelos_catalogo_marca_nome_unique unique (marca_id, nome)
);

create index if not exists idx_veiculo_marcas_catalogo_nome
  on public.veiculo_marcas_catalogo (nome);

create index if not exists idx_veiculo_modelos_catalogo_marca_id
  on public.veiculo_modelos_catalogo (marca_id);

create index if not exists idx_veiculo_modelos_catalogo_nome
  on public.veiculo_modelos_catalogo (nome);

create index if not exists idx_veiculo_modelos_catalogo_tipo
  on public.veiculo_modelos_catalogo (tipo);

