-- View pronta para buscas simples no frontend.
create or replace view public.vw_veiculo_catalogo as
select
  mo.id as modelo_id,
  ma.id as marca_id,
  ma.nome as marca,
  mo.nome as modelo,
  mo.tipo
from public.veiculo_modelos_catalogo mo
join public.veiculo_marcas_catalogo ma on ma.id = mo.marca_id
where ma.ativo = true
  and mo.ativo = true
order by ma.nome, mo.nome;

